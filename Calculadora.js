// Crear la calculadora que: Sumar치, Restar치, Dividir치 y Multiplicar치
// Creamos una clase
class Calculadora{
    sumar(num1,num2){
        return num1 + num2;
    }

    restar(num1,num2){
        return num1 - num2;
    }

    dividir(num1,num2){
        return num1 / num2;
    }

    multiplicar(num1,num2){
        return num1 * num2;
    }
}